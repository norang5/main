	AFTER
	INSERT ON B
	FOR EACH ROW
BEGIN
	DBMS_OUTPUT.PUT_LINE('변경 전:'||:OLD.NAME);
	DBMS_OUTPUT.PUT_LINE('변경 전:'||:OLD.AMOUNT);
	
	DBMS_OUTPUT.PUT_LINE('변경 후:'||:NEW.NAME);
	DBMS_OUTPUT.PUT_LINE('변경 후:'||:NEW.AMOUNT);
	
	UPDATE A
	SET AMOUNT = AMOUNT + :NEW.AMOUNT
	WHERE NAME = :NEW.NAME;
END;
